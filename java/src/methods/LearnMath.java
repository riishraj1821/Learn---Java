package methods;

public class LearnMath {
    public static void main(StringMethod[] args) {
        int a = 2;
        int b = 3;

        System.out.println(Math.min(a,b));
        System.out.println(Math.sqrt(9));
        System.out.println(Math.pow(3,4));//power of 3
        System.out.println((Math.abs(-2)));// absolute value
        System.out.println(Math.random());
        System.out.println(Math.floor(4.6));
        System.out.println(Math.ceil(4.4));
        System.out.println(Math.round(4.56));


    }
}
